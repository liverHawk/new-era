document.addEventListener('DOMContentLoaded', function() {
    const btn = document.getElementById('menu-float-btn');
    const menu = document.getElementById('float-menu');
    const close = document.getElementById('menu-close-btn');

    function updateMenuDisplay() {
        if (window.innerWidth <= 768) {
            btn.style.display = 'block';
            menu.style.display = 'none';
            document.querySelector('.menu').style.display = 'none';
        } else {
            btn.style.display = 'none';
            close.style.display = 'none';
            document.querySelector('.menu').style.display = 'block';
        }
    }

    btn.onclick = () => menu.style.display = 'block';
    close.onclick = () => menu.style.display = 'none';

    document.addEventListener('mousedown', function(e) {
    if (menu.style.display === 'block' && !menu.contains(e.target) && e.target !== btn) {
        menu.style.display = 'none';
    }
    });

    window.addEventListener('resize', updateMenuDisplay);
    updateMenuDisplay();
});